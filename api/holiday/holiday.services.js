const add__ = async (data) => {
    let {title, h_date}=data;
    let connection = await mysql.createConnection(dbconfig);
    try {
      connection = await mysql.createConnection(dbconfig);
      const [rows, fields] = await connection.execute(addQuery, [title, h_date]);
      return rows;
    } catch (error) {
      return false;
    } finally {
      connection.end();
    }
  };

